“software engineering” encompasses not just the act of writing code, 
but all of the tools and processes an organization uses to 
build and maintain that code over time.



software engineering tasks (development, modification, maintenance)




Your project is sustainable if, for the expected life span of your software, you
are capable of reacting to whatever valuable change comes along, for either technical or business reasons.


additional_feedback
When you are fundamentally incapable of reacting to a change in underlying technology or product direction, you’re
placing a high-risk bet on the hope that such a change never becomes critical.
For short-term projects, that might be a safe bet. Over multiple decades, it probably isn’t


The job of a software engineer, or a software
engineering leader, is to aim for sustainability and management of the scaling
costs for the organization, the product, and the development workflow.